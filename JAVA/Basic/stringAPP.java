package Basic;

public class stringAPP {
        public static void main(String[] args) {
            System.out.println("Hello World"); // 문자열
            System.out.println('H'); // 문자, 자바에서 ''는 한 문자만 표현하는 데이터타입
            System.out.println("H"); // 문자열, 문자와 문자열은 다르다

            System.out.println("Hello World\n");    // \n을 통해 줄바꿈
            System.out.println("Hello \"World\""); // 문자열 안에서 ""와 같은 특수기능이 있는 기호를 쓰려면 앞에 \를 써야됨


        }
}
